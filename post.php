			<h1>MediaMiser, an Innodata Company, Acquires Agility to Deliver Enhanced Media Intelligence Solutions for the Global PR Industry</h1>
				
			<span class="post-date"><i class="icon-calendar"></i>July 14, 2016</span>
			
			<p><em>Acquisition of Agility from PR Newswire gives MediaMiser a stronger footprint in the US and UK markets</em></p>
				
			<p><strong>NEW YORK - July 14, 2016</strong> - INNODATA INC. (NASDAQ: INOD) today announced that its MediaMiser subsidiary (https://www.mediamiser.com) has purchased Agility from PR Newswire. MediaMiser is a leading provider of media monitoring and analysis software and services. Agility is a leading global media contact database and media monitoring platform that helps customers target key influencers, amplify content and measure impact.</p>

			<p>MediaMiser acquired Agility to foster growth in North America and Europe by bolstering MediaMiser's media intelligence solutions and media databases, improving its media outreach capabilities, and delivering stronger, more data-powered media intelligence to clients. With the acquisition of Agility, MediaMiser is now one of only a handful of companies able to offer a global media contact dataset with integrated workflows for distribution, monitoring and measurement.</p>

			<p>Jack Abuhoff, Innodata's Chairman and CEO, said, "We will be offering the best of MediaMiser and Agility services to our combined customer base. Our existing MediaMiser customers will soon have access to Agility's truly global database of social media bloggers, influencers and journalists, important for amplifying messaging in a world that increasingly knows no boundaries. Current Agility customers, meanwhile, will gain access to MediaMiser's enterprise-level data insight tools and analysis services, which have contributed to MediaMiser enjoying one of the highest client renewal and retention rates in the media intelligence industry.</p>

			<p>"In addition, we will be expanding and deepening the Agility database using Innodata's database augmentation technology and research teams - the same technology and teams utilized by many of the world's leading information companies to build the world's best databases."</p>

			<p>"The media monitoring industry is experiencing rapid change amid ongoing digital media expansion. MediaMiser has been keen to capitalize on industry developments with smart additions and expansions to our portfolio," said Chris Morrison, President of MediaMiser. "Agility is an excellent product with an extensive global media contact database and a strong reputation in the marketplace. We're pleased to bring Agility into MediaMiser's integrated offering."</p>

			<p>The acquisition brings to MediaMiser more than 1,500 Agility customers and approximately 50 employees in the US and the UK. Agility had approximately $5 million in revenues in 2015.</p>

			<p>For additional information see the Report on Form 8-K to be filed by Innodata Inc. with the Securities and Exchange Commission with respect to the acquisition.</p>

			<p>To schedule an interview with Jack Abuhoff, CEO of Innodata or Chris Morrison, President of MediaMiser, please contact Vanessa Horwell at vhorwell@thinkinkpr.com in the UK or Nikkia Griffin at ngriffin@thinkinkpr.com in the US. For more information, please visit <a href="#">www.mediamiser.com</a> or <a href="#">www.agilitypr.com</a>.</p>

			<h2>About Innodata</h2>

			<p>Innodata (NASDAQ: INOD) is a global digital services and solutions company. Innodata's technology and services power leading information products and online retail destinations around the world. Innodata's solutions help prestigious enterprises harness the power of digital data to re-imagine how they operate and drive performance. Innodata serves publishers, media and information companies, digital retailers, banks, insurance companies, government agencies and many other industries.</p>

			<p>Founded in 1988, Innodata comprises a team of 5,000 diverse people in 8 countries who are dedicated to delivering services and solutions that help the world's businesses make better decisions.</p>

			<p>Recent Innodata honors include EContent Magazine's EContent 100, KMWorld Magazine's 100 Companies That Matter in Knowledge Management, the International Association of Outsourcing Professionals' (IAOP) Global Outsourcing Top 100, D&B India's Leading ITeS and BPO Companies and the Black Book of Outsourcing's Top List of Leading Outsourcing Providers to the Printing and Publishing Business.</p>

			<h2>About MediaMiser</h2>

			<p>MediaMiser (<a href="#">https://www.mediamiser.com</a>) is a leading provider of media monitoring and analysis software and managed services. Through innovative web-based and mobile solutions, MediaMiser reduces the time and effort it takes to gather, analyze and distribute valuable business intelligence extracted from traditional and social media sources. For organizations that prefer to outsource, MediaMiser provides detailed analysis reports and daily media briefings through an expert professional services team.</p>

